#include "ft_printf.h"

int	ft_hexalen(long long n)
{
	int		len;

	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		len++;
		n = -n;
	}
	while (n)
	{
		n /= 16;
		len++;
	}
	return (len);
}
